function calc_max(a,b,c)
{
    return Math.max(a,b,c)
}
function calc_min(a,b,c)
{
    return Math.min(a,b,c)
}
function calc_sin_a(a)
{
    return Math.sin(a*Math.PI/180).toFixed(4)
}
function calc_cos_a(a)
{
    return Math.cos(a*Math.PI/180).toFixed(4)
}
function calc_a_b(a,b)
{
    return Math.pow(a,b)
}